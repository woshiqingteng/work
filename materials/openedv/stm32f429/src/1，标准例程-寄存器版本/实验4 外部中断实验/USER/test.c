int main(void)
{   
	Stm32_Clock_Init(360,25,2,8);//设置时钟,180Mhz
	delay_init(180);		//初始化延时函数 
	uart_init(90,115200);	//串口初始化为115200
	LED_Init();		  		//初始化与LED连接的硬件接口
	EXTIX_Init();         	//初始化外部中断输入 
	LED0=0;					//先点亮红灯
	while(1)
	{
		printf("OK\r\n");	
		delay_ms(1000);	  
	}
}

















